    <script>
        $(document).ready(function() {
            function updateDiscountValueMax() {
                const discountType = $('#discount_type').val();
                const discountValue = $('#discount_value');

                if (discountType === 'percentage') {
                    discountValue.attr('max', 100);
                } else {
                    discountValue.attr('max', 999999);
                }
            }

            // Update max value when the page loads
            updateDiscountValueMax();

            // Update max value when discount type changes
            $('#discount_type').change(function() {
                updateDiscountValueMax();
            });
        });
    </script>


